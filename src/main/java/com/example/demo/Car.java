package com.example.demo;

public class Car {
	private int speed;
	private String color;
	private int taiya;
	
	public int getTaiya() {
		return taiya;
	}
	public void setTaiya(int taiya) {
		this.taiya = taiya;
	}
	public int getSpeed() {
		return speed;
	}
	public void setSpeed(int speed) {
		this.speed = speed;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	
	
}
